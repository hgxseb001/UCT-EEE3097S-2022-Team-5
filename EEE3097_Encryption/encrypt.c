uint32_t substitute(uint32_t input) 
{
    uint8_t in[4];
    in[0] = input >> 24;
    in[1] = (input >> 16) & 0x00FF;
    in[2] = (input >> 8) & 0x0000FF;
    in[3] = input & 0x000000FF;

    uint8_t sub1[4];
    sub1[0] = sbox1[in[0]];
    sub1[1] = sbox1[in[1]];
    sub1[2] = sbox1[in[2]];
    sub1[3] = sbox1[in[3]];

    uint8_t sub2[4];
    sub2[0] = sbox2[sub1[0]];
    sub2[1] = sbox2[sub1[1]];
    sub2[2] = sbox2[sub1[2]];
    sub2[3] = sbox2[sub1[3]];

    uint32_t output = ((sub2[0] << 24) | (sub2[1] << 16) | (sub2[2] << 8) | sub2[3]);
    return output;
}

